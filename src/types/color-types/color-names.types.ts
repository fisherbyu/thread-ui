export type SucessColors = 'success' | 'success-light';

export type ErrorColors = 'error' | 'error-light';

export type WarningColors = 'warning' | 'warning-light';

export type InfoColors = 'info' | 'info-light';

export type StatusColors = SucessColors | ErrorColors | WarningColors | InfoColors;
